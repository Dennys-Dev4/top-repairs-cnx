export function getFAQs(lang: Lang, slug: string): { q: string; a: string }[] {
  const faqs: { q: string; a: string }[] = [];
  for (let i = 1; i <= 4; i++) {
    const qKey = `faq_${slug}_q${i}`;
    const aKey = `faq_${slug}_a${i}`;
    const q = translations[lang]?.[qKey];
    if (q) {
      faqs.push({ q, a: t(lang, aKey) });
    }
  }
  return faqs;
}

